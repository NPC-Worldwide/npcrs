//! ## Module Map
//!
//! - `kernel/`    вҖ” Boot, process table, capability system
//! - `process/`   вҖ” NPC process lifecycle, resource limits
//! - `scheduler/` вҖ” Process scheduling, priority, token budgets
//! - `ipc/`       вҖ” Inter-process communication (pipes, signals, shared memory)
//! - `vfs/`       вҖ” Virtual filesystem (real FS + memory + KG)
//! - `drivers/`   вҖ” LLM providers, MCP servers, Python runtime as devices
//! - `npc/`       вҖ” NPC definition, loading
//! - `jinx/`      вҖ” Jinx (syscall) definitions, step execution
//! - `team/`      вҖ” Team loading (boot image)
//! - `llm/`       вҖ” Raw LLM HTTP client
//! - `memory/`    вҖ” SQLite history, knowledge graph
//! - `mcp/`       вҖ” MCP protocol client
//! - `shell/`     вҖ” User-space shell interface
//! - `template/`  вҖ” Tera template engine
//! - `ffi/`       вҖ” C-ABI for Flutter/Dart

